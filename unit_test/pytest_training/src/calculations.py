#Business logic or Dev code

class Calculations:
    def add(self, n1, n2):
        return n1 + n2

    def sub(self, n1, n2):
        return n1 - n2

    def mul(self, n1, n2):
        return n1 * n2

    def div(self, n1, n2):
        return n1 / n2

    def equal(self, n1, n2):
        return n1 == n2

    def area_of_square(self, side):
        return side * side

    def area_of_reat(self, l, b):
        return l * b